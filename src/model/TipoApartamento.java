package model;

public class TipoApartamento {

	private int pkTipoApt;
	private String tipoApt;
	private double valorDiaria;
	
	public int getPkTipoApt() {
		return pkTipoApt;
	}
	public void setPkTipoApt(int pkTipoApt) {
		this.pkTipoApt = pkTipoApt;
	}
	public String getTipoApt() {
		return tipoApt;
	}
	public void setTipoApt(String tipoApt) {
		this.tipoApt = tipoApt;
	}
	public double getValorDiaria() {
		return valorDiaria;
	}
	public void setValorDiaria(double valorDiaria) {
		this.valorDiaria = valorDiaria;
	}
	
	
	
}
